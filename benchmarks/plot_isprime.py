#!/usr/bin/env python3
# -*- coding: utf-8 -*-
import os
import sys
from typing import Tuple

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import matplotlib.lines as mlines
import numpy as np
from PIL import Image

file_type = "webp"

COLOR_1 = "#379777"
COLOR_2 = "#F4CE14"


def read_csv_fast(path: str) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    # Columns: n, is_prime, time_ns
    # Use numpy.loadtxt with converters to avoid Python-level loops
    data = np.loadtxt(path, delimiter=",", skiprows=1, dtype=np.float64)
    if data.ndim == 1:
        data = data.reshape(1, -1)
    n = data[:, 0]
    is_prime = data[:, 1]
    t = data[:, 2]
    return n, is_prime, t


def read_summary_fast(path: str) -> np.ndarray:
    # Generated by benchmarks/isprime_bench.cpp
    # Header uses commas, but the data rows are whitespace-separated.
    # Rows: bitlen=1..64
    # Cols: avg_time_prime_IsPrime, avg_time_prime_IsPrimeNoTable,
    #       avg_time_composite_IsPrime, avg_time_composite_IsPrimeNoTable
    data = np.loadtxt(path, delimiter=",", skiprows=1, dtype=np.float64)
    if data.ndim == 1:
        data = data.reshape(1, -1)
    return data


def save_scatter(
    n: np.ndarray,
    p: np.ndarray,
    t: np.ndarray,
    out_path: str,
    title: str,
    xlabel: str = "log2(n)",
    ylabel: str = "time [ns]",
) -> None:
    x = np.log2(n)
    y = t

    fig = plt.figure(figsize=(12, 7), dpi=150)
    ax = fig.add_subplot(1, 1, 1)

    cmap = matplotlib.colors.ListedColormap([COLOR_1, COLOR_2])
    ax.scatter(x, y, s=4, c=p, cmap=cmap, vmin=0, vmax=1, alpha=0.6, linewidths=0)

    prime_patch = mlines.Line2D(
        [],
        [],
        color=COLOR_2,
        marker="o",
        linestyle="None",
        markersize=6,
        label="Prime",
    )
    composite_patch = mlines.Line2D(
        [],
        [],
        color=COLOR_1,
        marker="o",
        linestyle="None",
        markersize=6,
        label="Composite",
    )
    ax.legend(handles=[prime_patch, composite_patch], loc="best")

    ax.set_xlabel(xlabel)
    ax.set_ylabel(ylabel)
    ax.set_title(title)

    # Grid and tight layout for readability
    ax.minorticks_on()
    ax.grid(True, linestyle=":", linewidth=1.0, alpha=0.6, which="major")
    ax.grid(True, linestyle=":", linewidth=0.5, alpha=0.3, which="minor")
    fig.tight_layout()

    fig.savefig(out_path + "." + file_type, format=file_type)
    plt.close(fig)


def save_summary_plots(summary: np.ndarray, out_prefix: str) -> None:
    # summary shape: (64, 4)
    x = np.arange(1, summary.shape[0] + 1)
    prime_isprime = summary[:, 0]
    prime_notable = summary[:, 1]
    comp_isprime = summary[:, 2]
    comp_notable = summary[:, 3]

    # Comparison plots (no log): keep readable via inset zoom panels.
    fig = plt.figure(figsize=(12, 8), dpi=120)
    ax1 = fig.add_subplot(2, 1, 1)
    ax2 = fig.add_subplot(2, 1, 2, sharex=ax1)

    for ax in (ax1, ax2):
        ax.minorticks_on()
        ax.grid(True, linestyle=":", linewidth=1.0, alpha=0.6, which="major")
        ax.grid(True, linestyle=":", linewidth=0.5, alpha=0.3, which="minor")
        ax.set_ylabel("avg time [ns]")

    prime_style_a = dict(
        color=COLOR_2,
        marker="o",
        markersize=3,
        linewidth=1.2,
        label="IsPrime",
    )
    prime_style_b = dict(
        color=COLOR_1,
        marker="o",
        markersize=3,
        linewidth=1.2,
        label="IsPrimeNoTable",
    )
    ax1.plot(x, prime_isprime, **prime_style_a)
    ax1.plot(x, prime_notable, **prime_style_b)
    ax1.set_title("Average time by bit-length (Prime)")
    ax1.legend(loc="best")

    comp_style_a = dict(
        color=COLOR_2,
        marker="o",
        markersize=3,
        linewidth=1.2,
        label="IsPrime",
    )
    comp_style_b = dict(
        color=COLOR_1,
        marker="o",
        markersize=3,
        linewidth=1.2,
        label="IsPrimeNoTable",
    )
    ax2.plot(x, comp_isprime, **comp_style_a)
    ax2.plot(x, comp_notable, **comp_style_b)
    ax2.set_title("Average time by bit-length (Composite)")
    ax2.set_xlabel("bit_width(n) (1..64)")
    ax2.legend(loc="best")

    fig.tight_layout()
    fig.savefig(out_prefix + "." + file_type, format=file_type)
    plt.close(fig)


def main() -> int:
    plt.style.use("dark_background")
    cwd = os.getcwd()

    p_prime = os.path.join(cwd, "benchmarks/bench_IsPrime.csv")
    p_notable = os.path.join(cwd, "benchmarks/bench_IsPrimeNoTable.csv")
    p_summary = os.path.join(cwd, "benchmarks/bench_summary.csv")

    if not os.path.exists(p_prime):
        print(f"Missing file: {p_prime}", file=sys.stderr)
        return 1
    if not os.path.exists(p_notable):
        print(f"Missing file: {p_notable}", file=sys.stderr)
        return 1
    if not os.path.exists(p_summary):
        print(f"Missing file: {p_summary}", file=sys.stderr)
        return 1

    n1, p1, t1 = read_csv_fast(p_prime)
    n2, p2, t2 = read_csv_fast(p_notable)
    summary = read_summary_fast(p_summary)

    save_scatter(
        n1,
        p1,
        t1,
        os.path.join(cwd, "benchmarks/bench_IsPrime"),
        title="cppr::IsPrime",
    )
    save_scatter(
        n2,
        p2,
        t2,
        os.path.join(cwd, "benchmarks/bench_IsPrimeNoTable"),
        title="cppr::IsPrimeNoTable",
    )

    save_summary_plots(summary, os.path.join(cwd, "benchmarks/bench_summary"))

    # Remove CSV files after generating plots
    os.remove(p_prime)
    os.remove(p_notable)
    os.remove(p_summary)

    # Optimize generated images
    for fname in [
        "benchmarks/bench_IsPrime." + file_type,
        "benchmarks/bench_IsPrimeNoTable." + file_type,
        "benchmarks/bench_summary." + file_type,
    ]:
        with Image.open(fname) as img:
            img.save(fname, optimize=True, quality=60)

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
